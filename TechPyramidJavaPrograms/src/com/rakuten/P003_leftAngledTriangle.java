package com.rakuten;

public class P003_leftAngledTriangle {

	public static void main(String[] args) {
		int n=5;
		for (int i=1;i<=n;i++) {
			for(int j=n;j>i;j--) {
				System.out.print(" ");
			
			for(j=1;j<=1;j++) {
				System.out.print("* ");
				
			}
			
			}
			System.out.println("");
		}
	}

}
